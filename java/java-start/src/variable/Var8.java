package variable;

public class Var8 {
    public static void main(String[] args) {
        byte b = 127;
        System.out.println("b = " + b);

        short s = 32767;
        System.out.println("s = " + s);

        int i = 2147483647;
        System.out.println("i = " + i);

        long l = 9223372036854775807L;
        System.out.println("l = " + l);


        float f = 3.14f;
        System.out.println("f = " + f);

        double d = 10.0;
        System.out.println("d = " + d);
    }
}
